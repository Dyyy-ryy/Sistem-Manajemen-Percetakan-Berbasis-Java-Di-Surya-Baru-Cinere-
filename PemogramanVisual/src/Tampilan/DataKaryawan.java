    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
 try {

        int baris = TabelK.getSelectedRow();

        if (baris == -1) {
            JOptionPane.showMessageDialog(null,
                    "Pilih data yang akan dihapus!");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(
                null,
                "Yakin ingin menghapus data ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi == JOptionPane.YES_OPTION) {

            Connection con = Koneksi.getConnection();

            String sql = "DELETE FROM karyawan WHERE Kode=?";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, KodeK.getText());

            int hasil = pst.executeUpdate();

            if (hasil > 0) {

                // Hapus baris dari JTable
                DefaultTableModel model =
                        (DefaultTableModel) TabelK.getModel();

                model.removeRow(baris);
                autonumber();
                // Kosongkan form
                
                NamaK.setText("");
                JK.setSelectedIndex(0);
                Tanggal.setDate(null);
                Telp.setText("");
                Alamat.setText("");

                JOptionPane.showMessageDialog(null,
                        "Data berhasil dihapus");
            }
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null,
                "Gagal hapus: " + e.getMessage());
    }        
    }//GEN-LAST:event_DeleteActionPerformed

