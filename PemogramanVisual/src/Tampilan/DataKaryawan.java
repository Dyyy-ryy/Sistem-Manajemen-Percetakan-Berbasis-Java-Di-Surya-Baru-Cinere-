    private void CariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CariActionPerformed
try {
       String cariKode = (String) JOptionPane.showInputDialog(
        null,
        "Masukkan Kode Karyawan:",
        "Cari Data",
        JOptionPane.QUESTION_MESSAGE,
        null,
        null,
        "KK"
);

        if (cariKode == null || cariKode.trim().isEmpty()) {
            return;
        }

        DefaultTableModel model = new DefaultTableModel(
            new Object[][]{},
            new String[]{"Kode", "Nama", "Jenis Kelamin", "Tanggal Lahir", "Telepon", "Alamat"}
        );

        Connection con = Koneksi.getConnection();
        String sql = "SELECT * FROM karyawan WHERE Kode LIKE ?";
        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, "%" + cariKode.trim() + "%");

        ResultSet rs = pst.executeQuery();

        boolean ditemukan = false;

        while (rs.next()) {
            ditemukan = true;

            String jk = rs.getString("JenisKelamin");
            String jenis;

            if (jk.equals("L")) {
                jenis = "Laki-Laki";
            } else {
                jenis = "Perempuan";
            }

            model.addRow(new Object[]{
                rs.getString("Kode"),
                rs.getString("Nama"),
                jenis,
                rs.getDate("Tanggal_Lahir"),
                rs.getString("Telepon"),
                rs.getString("Alamat")
            });
        }

        if (!ditemukan) {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan!");
        }

        TabelK.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }

   // TODO add your handling code here:
    }//GEN-LAST:event_CariActionPerformed

