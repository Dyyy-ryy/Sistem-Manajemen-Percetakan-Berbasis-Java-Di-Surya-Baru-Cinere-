    //TAMPILKAN KE JTABLE
    private void tampilData() {
    DefaultTableModel model = new DefaultTableModel(
        new Object[][]{},
        new String[]{
            "Kode Karyawan",
            "Nama Karyawan",
            "Jenis Kelamin",
            "Tanggal Lahir",
            "Telepon",
            "Alamat"
        }
    ) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // tabel tidak bisa diedit
        }
    };

    try {
        Connection con = Koneksi.getConnection();
        Statement st = con.createStatement();

        String sql = "SELECT * FROM karyawan";
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("Kode"),
                rs.getString("Nama"),
                rs.getString("JenisKelamin"),
                rs.getString("Tanggal_Lahir"),
                rs.getString("Telepon"),
                rs.getString("Alamat")
            });
        }

        TabelK.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(
            null,
            "Gagal menampilkan data: " + e.getMessage()
        );
    }
}
    
  //AUTO GENERATE KODE
private void autonumber() {
    try {
        Connection con = Koneksi.getConnection();

        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(
            "SELECT Kode FROM karyawan ORDER BY Kode DESC LIMIT 1"
        );

        if (rs.next()) {
            String kode = rs.getString("Kode"); // contoh KK07
            int nomor = Integer.parseInt(kode.substring(2)); // 07

            nomor++;

            KodeK.setText(String.format("KK%02d", nomor));
        } else {
            KodeK.setText("KK01");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}
