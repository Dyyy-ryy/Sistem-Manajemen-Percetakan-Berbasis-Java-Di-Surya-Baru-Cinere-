    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
try {
        Connection con = Koneksi.getConnection();

        String sql = "INSERT INTO karyawan "
                + "(Kode, Nama, JenisKelamin, Tanggal_Lahir, Telepon, Alamat) "
                + "VALUES (?,?,?,?,?,?)";

        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, KodeK.getText());
        pst.setString(2, NamaK.getText());

        // konversi ComboBox ke ENUM database
        String jk = "";
        if (JK.getSelectedItem().toString().equals("Laki-Laki")) {
            jk = "L";
        } else if (JK.getSelectedItem().toString().equals("Perempuan")) {
            jk = "P";
        }

        pst.setString(3, jk);

        // tanggal lahir
        java.util.Date tanggal = Tanggal.getDate();

        if (tanggal == null) {
            JOptionPane.showMessageDialog(null, "Tanggal lahir belum dipilih!");
            return;
        }

        java.sql.Date sqlDate = new java.sql.Date(tanggal.getTime());
        pst.setDate(4, sqlDate);

        pst.setString(5, Telp.getText());
        pst.setString(6, Alamat.getText());

        pst.executeUpdate();
        autonumber();
        JOptionPane.showMessageDialog(null, "Data berhasil ditambah");
        tampilData();
        // reset form
        NamaK.setText("");
        JK.setSelectedIndex(0);
        Tanggal.setDate(null);
        Telp.setText("");
        Alamat.setText("");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal simpan: " + e.getMessage());
       
    }

    }//GEN-LAST:event_SimpanActionPerformed

