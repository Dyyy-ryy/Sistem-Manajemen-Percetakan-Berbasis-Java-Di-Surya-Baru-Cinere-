    private void cariKPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariKPActionPerformed
String input = JOptionPane.showInputDialog(
        null,
        "Masukkan Kode Pelanggan (tanpa KP):",
        "Cari Data",
        JOptionPane.QUESTION_MESSAGE
);

if (input == null || input.trim().isEmpty()) {
    return;
}

String kode = "KP" + input;

    if (kode == null || kode.trim().isEmpty()) {
        return;
    }

    try {
        Connection con = Koneksi.getConnection();

        String sql = "SELECT NamaP FROM pelanggan WHERE KodeP = ?";
        PreparedStatement St = con.prepareStatement(sql);
        St.setString(1, kode);

        ResultSet Rs = St.executeQuery();

        if (Rs.next()) {
            KP.setText(kode);
            NamaPM.setText(Rs.getString("NamaP"));
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan!");
           NamaPM.setText("");
        }

        Rs.close();
        St.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }        // TODO add your handling code here:
    }//GEN-LAST:event_cariKPActionPerformed

