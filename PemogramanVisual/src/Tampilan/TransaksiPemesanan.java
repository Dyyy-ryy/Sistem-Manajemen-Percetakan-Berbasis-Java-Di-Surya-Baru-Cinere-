    private void hpsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hpsActionPerformed
int konfirmasi = JOptionPane.showConfirmDialog(
            null,
            "Yakin ingin menghapus data ini?",
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {

        try {
            Connection con = Koneksi.getConnection();

            String sql = "DELETE FROM transaksipemesanan WHERE NoTransaksi=?";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, NT.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");

            // Kosongkan form
            NT.setText("");
            KP.setText("");
            NamaPM.setText("");
            KBP.setText("");
            JBP.setText("");
            HS.setText("");
            JMLP.setText("");
            TOTALP.setText("");
            TGL.setDate(null);

            // Hapus baris dari JTable
            int baris = tbl.getSelectedRow();
            if (baris != -1) {
                ((DefaultTableModel) tbl.getModel()).removeRow(baris);
            }
            autonumber();
            tampilData();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    null,
                    "Gagal menghapus data: " + e.getMessage());
        }
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_hpsActionPerformed

