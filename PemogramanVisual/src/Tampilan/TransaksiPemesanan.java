    //TAMPILKAN DATA KE JTABLE
private void tampilData() {

    DefaultTableModel model = new DefaultTableModel(
        new Object[][]{},
        new String[]{
    "No Transaksi",
    "Tanggal",
    "Jam",
    "Nama",
    "Kode Barang",
    "Kode Pemesanan",
    "Jenis Barang",
    "Harga Satuan",
    "Jumlah",
    "Total"
}
        
    ) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };

    try {
        Connection con = Koneksi.getConnection();
        Statement st = con.createStatement();

        String sql = "SELECT t.NoTransaksi, t.Tanggal, t.Jam, "
                + "t.KodePesanan, p.NamaP, "
                + "t.KodeBR, b.JenisB, "
                + "t.Jumlah, b.Harga, t.Total "
                + "FROM transaksipemesanan t "
                + "JOIN pelanggan p ON t.KodePesanan = p.KodeP "
                + "JOIN databarang b ON t.KodeBR = b.KodeB";

        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("NoTransaksi"),
                rs.getString("Tanggal"),
                rs.getString("Jam"),
                rs.getString("NamaP"),
                rs.getString("KodeBR"),
                rs.getString("KodePesanan"),
                rs.getString("JenisB"),
                rs.getInt("Harga"),
                rs.getInt("Jumlah"),
                rs.getInt("Total")
            });
        }

        tbl.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal: " + e.getMessage());
    }
}


//AUTO GENERATE KODE
private void autonumber() {
    try {
        Connection con = Koneksi.getConnection();

        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(
            "SELECT NoTransaksi FROM transaksipemesanan ORDER BY NoTransaksi DESC LIMIT 1"
        );

        if (rs.next()) {

            String kode = rs.getString(1).trim(); // penting: trim biar tidak error

            // ambil angka setelah huruf T
            String angka = kode.replaceAll("[^0-9]", "");

            int nomor = Integer.parseInt(angka);

            nomor++;

            NT.setText(String.format("T%02d", nomor));

        } else {
            NT.setText("T01");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}

