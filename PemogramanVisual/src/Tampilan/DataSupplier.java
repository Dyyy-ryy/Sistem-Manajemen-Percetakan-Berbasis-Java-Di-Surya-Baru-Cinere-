    //TAMPILKAN KE JTABLE
    private void tampilData() {
    DefaultTableModel model = new DefaultTableModel(
        new Object[][]{},
        new String[]{"Kode", "Nama", "Barang", "Alamat", "Telepon"}
    ) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // tidak bisa diedit
        }
    };

    try {
        Connection conn = Koneksi.getConnection();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM suplier");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("KodeS"),
                rs.getString("NamaS"),
                rs.getString("BarangS"),
                rs.getString("AlamatS"),
                rs.getString("TelpS")
            });
        }

        TabelS.setModel(model);

    } catch (Exception e) {
        System.out.println("Error tampil: " + e.getMessage());
    }
}
    
    
    //AUTO GENERATE KODE
 private void autonumber() {
    try {
        Connection con = Koneksi.getConnection();

        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(
            "SELECT KodeS FROM suplier ORDER BY KodeS DESC LIMIT 1"
        );

        if (rs.next()) {

            String kode = rs.getString("KodeS"); // contoh KB01
            int nomor = Integer.parseInt(kode.substring(2));

            nomor++;

            KS.setText(String.format("KS%02d", nomor));

        } else {

            KS.setText("KS01");

        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}
    
