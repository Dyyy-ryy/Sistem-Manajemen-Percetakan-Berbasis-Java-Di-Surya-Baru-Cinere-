    private void bt_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cariActionPerformed
        try {
        // popup input
        String cariKode = JOptionPane.showInputDialog(
            null,
            "Masukkan Kode Supplier:",
            "Cari Data",
            JOptionPane.QUESTION_MESSAGE
        );

        // jika cancel / kosong
        if (cariKode == null || cariKode.trim().isEmpty()) {
            tampilData(); // tampil semua lagi
            return;
        }

        DefaultTableModel model = new DefaultTableModel(
            new Object[][]{},
            new String[]{"Kode", "Nama", "Barang", "Alamat", "Telepon"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        Connection con = Koneksi.getConnection();
        String sql = "SELECT * FROM suplier WHERE KodeS LIKE ?";
        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, "%" + cariKode.trim() + "%");

        ResultSet rs = pst.executeQuery();

        boolean ditemukan = false;

        while (rs.next()) {
            ditemukan = true;

            model.addRow(new Object[]{
                rs.getString("KodeS"),
                rs.getString("NamaS"),
                rs.getString("BarangS"),
                rs.getString("AlamatS"),
                rs.getString("TelpS")
            });
        }

        if (!ditemukan) {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan!");
            tampilData();
        } else {
            TabelS.setModel(model);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal cari: " + e.getMessage());
    }// TODO add your handling code here:   // TODO add your handling code here:
    }//GEN-LAST:event_bt_cariActionPerformed

