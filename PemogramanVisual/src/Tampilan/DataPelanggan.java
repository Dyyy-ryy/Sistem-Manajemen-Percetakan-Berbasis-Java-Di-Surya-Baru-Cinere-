    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
 try {
        Connection con = Koneksi.getConnection();

        String sql = "UPDATE pelanggan SET "
                   + "NamaP=?, AlamatP=?, Email=?, Telp=? "
                   + "WHERE KodeP=?";

        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, NamaP.getText());
        pst.setString(2, AlamatP.getText());
        pst.setString(3, EmailP.getText());
        pst.setString(4, TelpP.getText());
        pst.setString(5, KodeP.getText().trim()); // kunci update

        int hasil = pst.executeUpdate();

        if (hasil > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil diupdate");
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan!");
        }

        // refresh tabel
        autonumber();
       tampilData();

        
        NamaP.setText("");
        AlamatP.setText("");
        EmailP.setText("");
        TelpP.setText("");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal update: " + e.getMessage());
    }
    }//GEN-LAST:event_EditActionPerformed

