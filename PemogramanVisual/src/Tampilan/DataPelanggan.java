    //TAMPILKAN KE JTABLE
    private void tampilData() {
    DefaultTableModel model = new DefaultTableModel(
        new Object[][]{},
        new String[]{
            "Kode Pelanggan",
            "Nama Pelanggan",
            "Alamat",
            "Email",
            "Telepon"
        }
    ) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // tabel tidak bisa diedit
        }
    };

    try {
        Connection con = Koneksi.getConnection();
        Statement st = con.createStatement();

        String sql = "SELECT * FROM pelanggan";
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("KodeP"),
                rs.getString("NamaP"),
                rs.getString("AlamatP"),
                rs.getString("Email"),
                rs.getString("Telp")
            });
        }

        TabelH.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(
            null,
            "Gagal menampilkan data: " + e.getMessage()
        );
    }
}
    
    
    //AUTO GENERATE KODE
    private void autonumber() {
    try {
        Connection con = Koneksi.getConnection();

        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(
            "SELECT KodeP FROM pelanggan ORDER BY KodeP DESC LIMIT 1"
        );

        if (rs.next()) {

            String kode = rs.getString("KodeP"); // contoh KB01
            int nomor = Integer.parseInt(kode.substring(2));

            nomor++;

            KodeP.setText(String.format("KP%02d", nomor));

        } else {

            KodeP.setText("KP01");

        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}
    
