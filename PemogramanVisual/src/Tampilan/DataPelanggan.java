    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
      int konfirmasi = JOptionPane.showConfirmDialog(
            null,
            "Yakin ingin menghapus data ini?",
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {

        try {
            Connection con = Koneksi.getConnection();

            String sql = "DELETE FROM pelanggan WHERE KodeP=?";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, KodeP.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");

            // Kosongkan form
            KodeP.setText("");
            NamaP.setText("");
            AlamatP.setText("");
            TelpP.setText("");
            EmailP.setText("");

            // Hapus baris dari JTable tanpa reload
            int baris = TabelH.getSelectedRow();
            if (baris != -1) {
                ((DefaultTableModel) TabelH.getModel()).removeRow(baris);
            }

            // Jika pakai autonumber
            tampilData();
            autonumber();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    "Gagal menghapus data: " + e.getMessage());
        }
    }  // TODO add your handling code here:
    }//GEN-LAST:event_HapusActionPerformed

