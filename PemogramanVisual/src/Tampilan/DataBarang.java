    //Tampilkan ke Jtable
    private void tampilData() {
    DefaultTableModel model = new DefaultTableModel(
    new Object[][]{},
    new String[]{"Kode", "Nama", "Jenis", "Harga"}
) {
    @Override
    public boolean isCellEditable(int row, int column) {
        return false; // 🔥 biar tidak bisa diedit
    }
};

    try {
        Connection conn = Koneksi.getConnection();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM databarang");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("KodeB"),
                rs.getString("NamaB"),
                rs.getString("JenisB"),
                rs.getInt("Harga")
            });
        }

        TabelB.setModel(model);

    } catch (Exception e) {
        System.out.println("Error tampil: " + e.getMessage());
    }
}

    
    //auto generate kode, contoh : KB01 DST
    private void autonumber() {
    try {
        Connection con = Koneksi.getConnection();

        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(
            "SELECT KodeB FROM databarang ORDER BY KodeB DESC LIMIT 1"
        );

        if (rs.next()) {

            String kode = rs.getString("KodeB"); // contoh KB01
            int nomor = Integer.parseInt(kode.substring(2));

            nomor++;

            Kode.setText(String.format("KB%02d", nomor));

        } else {

            Kode.setText("KB01");

        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}
    
