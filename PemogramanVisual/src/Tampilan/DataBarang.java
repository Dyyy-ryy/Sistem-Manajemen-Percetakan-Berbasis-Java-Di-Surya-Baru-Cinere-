    private void bt_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_hapusActionPerformed
try {
        // validasi
        if (Kode.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data yang mau dihapus!");
            return;
        }

        // konfirmasi
        int jawab = JOptionPane.showConfirmDialog(
            null,
            "Yakin ingin menghapus data ini?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION
        );

        if (jawab == JOptionPane.YES_OPTION) {

            Connection con = Koneksi.getConnection();
            String sql = "DELETE FROM databarang WHERE KodeB=?";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, Kode.getText().trim());

            int hasil = pst.executeUpdate();

            if (hasil > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan!");
            }

            tampilData(); // refresh tabel
            autonumber();
            // kosongkan form
           
            Nama.setText("");
            Harga.setText("");
            t_jb.setSelectedIndex(0);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal hapus: " + e.getMessage());
    }  // TODO add your handling code here:
    }//GEN-LAST:event_bt_hapusActionPerformed

